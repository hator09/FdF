#include "libft.h"

static char	*ft_strcpy(char *dest, const char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(const char *src)
{
	char	*s;
	int		len;

	len = ft_strlen(src);
	s = (char *)malloc(len + 1);
	if (s == 0)
		return (0);
	ft_strcpy(s, src);
	return (s);
}
